/// Spawns a command and waits up to `timeout` for it to finish.
/// If it exceeds the timeout, kills the child and returns `Err`.
///
/// Does not capture stdout/stderr — output goes to the parent's streams.
/// Do not use with `Stdio::piped()` as that risks pipe-buffer deadlock.
#[cfg(any(target_os = "macos", test))]
fn run_with_timeout(
    cmd: &mut std::process::Command,
    timeout: std::time::Duration,
) -> anyhow::Result<std::process::ExitStatus> {
    let program = cmd.get_program().to_string_lossy().to_string();
    let mut child = cmd.spawn()?;
    let start = std::time::Instant::now();
    loop {
        match child.try_wait()? {
            Some(status) => return Ok(status),
            None => {
                if start.elapsed() >= timeout {
                    if let Err(e) = child.kill() {
                        log::warn!("run_with_timeout: kill failed: {e}");
                    }
                    let _ = child.wait();
                    anyhow::bail!(
                        "command '{}' timed out after {}s",
                        program,
                        timeout.as_secs()
                    );
                }
                std::thread::sleep(std::time::Duration::from_millis(200));
            }
        }
    }
}

pub fn factory_reset() -> anyhow::Result<()> {
    let state = SetupState::load();

    // 1. Stop containers for the active project (if any) — with timeout
    //    Even is_available() could theoretically hang, so run the entire
    //    "check + compose_down" block with a timeout.
    if let Some(ref project) = state.project_created {
        log::info!("stopping containers for project={project}");
        let project_clone = project.clone();
        // Uses thread+channel (not run_with_timeout) because compose_down goes
        // through the ContainerRuntime trait, which returns Result — not a Command.
        let (tx, rx) = std::sync::mpsc::channel();
        std::thread::spawn(move || {
            let rt = runtime::detect_runtime();
            if rt.is_available() {
                if let Err(e) = rt.compose_down(&project_clone) {
                    log::warn!("compose_down failed: {e}");
                }
            }
            let _ = tx.send(());
        });
        // Wait up to 30s; if anything hangs, limactl delete --force will clean up
        let timeout = std::time::Duration::from_secs(30);
        match rx.recv_timeout(timeout) {
            Ok(()) => {}
            Err(std::sync::mpsc::RecvTimeoutError::Timeout) => {
                log::warn!("compose_down timed out after 30s, continuing");
            }
            Err(std::sync::mpsc::RecvTimeoutError::Disconnected) => {
                log::warn!("compose_down thread panicked, continuing");
            }
        }
    }

    // 2. Destroy VM (macOS only) — force-stop then force-delete, each with timeout
    #[cfg(target_os = "macos")]
    {
        let timeout = std::time::Duration::from_secs(30);

        log::info!("stopping VM");
        let mut stop_cmd = limactl_command();
        stop_cmd.args(["stop", "--force", consts::LIMA_VM_NAME]);
        if let Err(e) = run_with_timeout(&mut stop_cmd, timeout) {
            log::warn!("limactl stop timed out or failed: {e}, continuing");
        }

        log::info!("deleting VM");
        let mut delete_cmd = limactl_command();
        delete_cmd.args(["delete", consts::LIMA_VM_NAME, "--force"]);
        if let Err(e) = run_with_timeout(&mut delete_cmd, timeout) {
            log::warn!("limactl delete timed out or failed: {e}, continuing");
        }
    }

    // 3. Remove CLI binary
    #[cfg(unix)]
    {
        let target = dirs::home_dir()
            .ok_or_else(|| anyhow::anyhow!("cannot determine home directory"))?
            .join(".local")
            .join("bin")
            .join(consts::CLI_BINARY);
        let _ = std::fs::remove_file(&target);
    }
    #[cfg(target_os = "windows")]
    {
        let target = dirs::home_dir()
            .ok_or_else(|| anyhow::anyhow!("cannot determine home directory"))?
            .join(consts::DATA_DIR)
            .join("bin")
            .join("speedwave.exe");
        let _ = std::fs::remove_file(&target);
    }

    // 4. Wipe setup state + project list (tokens on disk are preserved)
    let data_dir = dirs::home_dir()
        .ok_or_else(|| anyhow::anyhow!("cannot determine home directory"))?
        .join(consts::DATA_DIR);
    wipe_state_and_config(&data_dir)?;

    Ok(())
}

/// Resets `setup_state.json` and `config.json` inside `data_dir` to defaults.
///
/// Tokens stored in `data_dir/tokens/` are intentionally preserved.
fn wipe_state_and_config(data_dir: &std::path::Path) -> anyhow::Result<()> {
    std::fs::create_dir_all(data_dir)?;

    // Reset setup state (atomic write via rename)
    let state_path = data_dir.join("setup_state.json");
    let default_state = SetupState::default();
    let state_json = serde_json::to_string_pretty(&default_state)?;
    let tmp_state = state_path.with_extension("json.tmp");
    std::fs::write(&tmp_state, &state_json)?;
    std::fs::rename(&tmp_state, &state_path)?;

    // Reset project list (atomic write via rename)
    let config_path = data_dir.join("config.json");
    let default_config = config::SpeedwaveUserConfig::default();
    let config_json = serde_json::to_string_pretty(&default_config)?;
    let tmp_config = config_path.with_extension("json.tmp");
    std::fs::write(&tmp_config, &config_json)?;
    std::fs::rename(&tmp_config, &config_path)?;

    Ok(())
}

