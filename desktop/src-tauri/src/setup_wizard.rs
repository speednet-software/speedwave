pub fn build_images() -> anyhow::Result<()> {
    let rt = runtime::detect_runtime();
    rt.ensure_ready()?;
    build::build_all_images(rt.as_ref())?;

    let mut state = SetupState::load();
    state.images_built = true;
    state.save()?;

    Ok(())
}

